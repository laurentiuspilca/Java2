    public MainFrame() {
        initComponents();
        
        model = new DefaultListModel<>();
        jList1.setModel(model);
        jList1.addMouseListener(new MouseAdapter() {
            
            @Override
            public void mouseClicked(MouseEvent ev) {
                if (ev.getClickCount() == 2) {
                    Produs selected = jList1.getSelectedValue();
                    stergeProdus(selected);
                    afisareProduse();
                }
            }
        });
       
        afisareProduse();
        
        setLocationRelativeTo(null);
        setVisible(true);
    }
    
    private void stergeProdus(Produs p) {
        ProdusWebServiceController.getInstance().stergeProdus(p);
    }
    
    private void afisareProduse() {
        model.clear();
        List<Produs> produse = ProdusWebServiceController.getInstance().getProduse();
        produse.forEach(model::addElement);
    }
    
