    private void register () {
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
    
        try {
            if (ConnectionController.getInstance()
                    .register(username, password)) {
                JOptionPane.showMessageDialog(null, "You are registered!");
            } else {
                JOptionPane.showMessageDialog(null, "You are already registered!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Boo! You got an error!");
        }
    }
    
    private void login () {
        try {
            String username = jTextField1.getText();
            String password = new String(jPasswordField1.getPassword());
            
            
            if (ConnectionController
                    .getInstance()
                    .login(username, password)) {
                new MainFrame();
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Invalid username or password!");
            }
        } catch (RemoteException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Boo! You got an error!");
        }
    }

